                    <h3 style="font-weight:bold;" > RISK OF SELLING A CAR WITHOUT PAPER WORK</h3>
                    <hr style="height:4px; width:700px;">
                    <p class="font">
                        It is common to resale a car to second hand or even third hand owner. Car ownership transfer is a time consuming process. This leads to avoid of paper work while selling a car. But selling a car without paper work is risky as explained below. Also see (is it safe to sell your car to any Mayapuri scrap Dealer)
                    </p>
                    <ul class="font">
                        <li>New owner can use it in illegal way or for criminal activities, the actual owner may be considered involved in illegal or criminal activity.</li>
                        <li>New owner is a trustworthy person but s/he further sells or lends the car to someone who uses it illegally. This is a common case, specially when a car is sold to a person with rural background. As it is quite common in rural India to lend or even sell a car without paperwork.</li>
                        <li>All the challan will still reach your home. Challan are always send to RC address. And legal responsibility of paying the challan will still lie with you.</li>
                        <li>You will be directly contacted by law enforcement bodies for questioning for misuse. Like when police investigates crime involving any motor vehicle, the owner is the first person questioned and treated like a suspect.</li>
                        <li>You need to get involved in insurance claim/accidental claims. As insurance claim requiring signing papers. The owner may also be contacted by insurance surveying agency for questioning regarding the claim. Also insurance policy is not valid as per terms mentioned in insurance. Only third party insurance will be considered valid and validity of comprehensive insurance will be void.</li>
                        <li>The original owner will be responsible for third party loss in case of insurance is expired. It is common for third party victims of accidental loss to claim loss and other compensation. Owner will be liable for these claims.</li>
                    </ul>
                    <h4 style="font-weight:bold;">Selling a car to a used car dealer</h4>
                    <ol class="font">
                        <li>Ensures the dealer is trustworthy and reputed. Don’t sell your car to a dealer without background check.</li>
                        <li>Ensures that dealer don’t sell this car to any other dealer. Some dealers sale cars further to other dealers in other states or remote areas. It is risky as you cannot verify the background of a new dealer.</li>
                        <li>Dealer should sign an assurance note mentioning date and time of sale. Assurance note should be retained by even after transfer of RC. This will make you safe from issue arising due to some indecent which occurred after sale and before transfer of the car.</li>
                        <li>It is also good to get signed proof of sale like receipt. This will further strengthen the assurance note and can be considered as a proof in court of law.</li>
                        <li>Keep asking status of car from dealer. You are entitled to know if car is successfully transferred or still parked at dealer end.</li>
                    </ol>
                    <h4 style="font-weight:bold;">Selling a car directly to a new owner</h4>
                    <ol class="font">
                        <li>Always do background check, like verification of ID proof of new owner before making a deal. You should be particularly careful while selling your car online using old product listing services. These sites also mention safety procedure for seller, you must read the mentioned guidelines.</li>
                        <li>Before handing over the car ensure application for transfer has been submitted to RTO and you have atleast one signed proof of sale.</li>
                    </ol>
                    <h4 style="font-weight:bold;">Selling a car for scrap</h4>
                    <ol class="font">
                        <li>You are handed over an assurance note that car will not be used for running on road and it will be scrapped.</li>
                        <li>Chassis number is cut out from car and handed over to you or to RTO for cancellation of your RC.</li>
                        <li>You should also ensure that your car is actually scrapped, in case of resale of whole car the RC must be transferred.</li>
                        <li>Also ensure car scrap dealer is giving you competitive price for your scrap car. Check our <a href="car_scrap_price.php">prices for scrap car</a>.</li>
                    </ol>
